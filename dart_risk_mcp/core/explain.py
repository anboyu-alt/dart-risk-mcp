def _metric_amendments(flag: str, metric: dict | None) -> str:
    """metric dict가 있을 때 본문에 덧붙일 '이번 분석의 수치' 문장."""
    if not metric:
        return ""
    if flag in ("AR_SURGE", "INVENTORY_SURGE"):
        cur = metric.get("current")
        pri = metric.get("prior")
        delta = metric.get("delta")
        if cur is None or pri is None or delta is None:
            return ""
        return (
            f"\n\n이번 분석: 이 비율이 전년 {pri:.1f}%에서 {cur:.1f}%로 "
            f"{delta:+.1f}%포인트 움직였습니다."
        )
    if flag == "CASH_GAP":
        ni = metric.get("current_ni")
        ocf = metric.get("current_ocf")
        if ni is None or ocf is None:
            return ""
        return (
            f"\n\n이번 분석: 당기 순이익 {ni:,}원, 영업활동 현금흐름 "
            f"{ocf:,}원으로 두 수치가 같은 방향을 가리키지 않습니다."
        )
    if flag == "CAPITAL_IMPAIRMENT":
        cur = metric.get("current")
        if cur is None:
            return ""
        return f"\n\n이번 분석: 자본총계가 자본금의 {cur:.0f}% 수준입니다."
    if flag == "CAPITAL_CHURN":
        total = metric.get("total_events")
        max12m = metric.get("max_12m_count")
        if total is None and max12m is None:
            return ""
        parts = []
        if total is not None:
            parts.append(f"전체 자본 이벤트 {total}건")
        if max12m is not None:
            parts.append(f"12개월 최대 집중도 {max12m}건")
        return "\n\n이번 분석: " + ", ".join(parts) + "."
    return ""


def flag_to_prose(flag: str, metric: dict | None = None) -> tuple[str, str]:
    """flag → (제목, 본문). 모르는 flag면 (flag, "") 폴백."""
    entry = FLAG_PROSE.get(flag)
    if not entry:
        return flag, ""
    body = entry["body"] + _metric_amendments(flag, metric)
    return entry["title"], body


