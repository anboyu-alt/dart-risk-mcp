def get_sector_for_induty_code(induty_code: str | None) -> str | None:
    """KSIC 업종코드에서 대분류 알파벳을 반환한다. 매핑 없으면 None."""
    if not induty_code:
        return None
    prefix = str(induty_code)[:2]
    return KSIC_TO_SECTOR.get(prefix)


def get_induty_name(induty_code: str | None) -> str:
    """KSIC 업종코드에서 표시용 업종명을 반환한다."""
    if not induty_code:
        return "미분류"
    prefix = str(induty_code)[:2]
    return KSIC_NAMES.get(prefix, f"업종코드 {induty_code}")


def get_critical_items(induty_code: str | None) -> list[tuple]:
    """업종코드에 해당하는 유의 회계정책 목록을 반환한다.

    업종 매핑이 없으면 공통 DEFAULT 항목만 반환.

    Returns:
        [(item_key, 표시명, priority, 설명), ...]
    """
    sector = get_sector_for_induty_code(induty_code)
    items: list[tuple] = []
    if sector and sector in SECTOR_POLICY_MAP:
        items = list(SECTOR_POLICY_MAP[sector]["critical_items"])
    # 공통 항목 중 중복되지 않는 것만 추가
    existing_keys = {it[0] for it in items}
    for default_item in DEFAULT_POLICY_ITEMS:
        if default_item[0] not in existing_keys:
            items.append(default_item)
    return items
